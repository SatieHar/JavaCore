package classwork.chapter3;

public class ArraySample2 {
    public static void main(String[] args) {
        int [] numbers = new int [30];
        for (int i = 0; i < 30 ; i++) {
            numbers [i] = i + 1;
        }
        for (int i = 0; i < 30; i++) {
            System.out.print(numbers[i] + " ");
        }
    }
    //Another example (shorter)
}
